public class EjrecicioTablaMultiplicacion {

    public static void main(String[] args) {

        for(int contador = 0; contador <=10; contador++){
            for (int multi=0;multi<=10;multi++){
                System.out.println(contador*multi);
                System.out.println(" ");
            }
            System.out.println();
        }
    }
}
